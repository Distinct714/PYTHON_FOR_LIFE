# NONE TYPE

# The none is used to define a null value or object in Python. 
# It is not the same as an empty string, a False, or a zero but return none only.
y = None
print(y)

# SAMPLE ONE
x = None

if x:
  print("This is nothing.")
elif x is False:
  print ("There is something here.")
else:
  print("Neither there is something here or none.")
  
