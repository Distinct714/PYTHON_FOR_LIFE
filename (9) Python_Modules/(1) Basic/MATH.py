# MATH MODULE

# Python has also a built-in module called math, which extends the list of mathematical functions.

from math import*

# floor()   This method rounds a number upwards to its nearest integer
print(floor(10.4))

# ceil()    This method rounds a number downwards to its nearest integer, and returns the result:
print(ceil(9.6))

# sqrt()    This function returns the square root of given number.
print(sqrt(64))

# The math.pi function returns the value of PI (3.14...)
import math as m

def PI():
    x = m.pi
    print(x)
PI()

