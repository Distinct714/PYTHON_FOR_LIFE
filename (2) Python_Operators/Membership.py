# MEMBERSHIP OPERATORS

# To verify if a certain characters or values are present in a string, we can use the keyword in.

phrase = "Joshua Restaurant is delicious"
print("is" in phrase)

# You can also apply conditional statements here.

shape = ["Circle", "Square", "Triangle"]

if "Circle" in shape:
    print("Present")

# To verify if a certain characters or values are not present in a string, we can use the keyword not in.

phrase = "Joshua Restaurant is delicious"
print("are" not in phrase)

